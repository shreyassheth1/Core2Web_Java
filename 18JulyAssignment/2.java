class Second{
	public static void main(String[] args) {
		int num = 100;
		System.out.println("num>>>=2 ="+(num>>>=2));
		System.out.println("num<<=2 ="+(num<<=2));
		System.out.println("num^=2 ="+(num^=2));
		System.out.println("num=++num ="+(num=++num));
		System.out.println("num=~num ="+(num=~num));
	}
}